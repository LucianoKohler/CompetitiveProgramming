#include<bits/stdc++.h>
using namespace std;
#define ll long long
const ll modulo = 1e9+7;
const int mx = 2e5+5; // This varies!

int main(){
    cin.tie(0) -> sync_with_stdio(0);
    int t; cin >> t;
    while(t--){
      vector<string>grid;

      for(int i = 0; i < 8; i++){
        string s; cin >> s;
        grid.push_back(s);
      }

      for(int i = 1; i <= 6; i++){
        for(int j = 1; j <= 6; j++){
          if(
            grid[i+1][j+1] == '#' && 
            grid[i+1][j-1] == '#' &&
            grid[i-1][j+1] == '#' && 
            grid[i-1][j-1] == '#'){
              cout << i+1 << " " << j+1 << "\n";
            }
        }
      }
    }
    return 0;
}
