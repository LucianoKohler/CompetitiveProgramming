#include<bits/stdc++.h>
using namespace std;
#define ll long long
const ll modulo = 1e9+7;
const int mx = 2e5+5;
string s;
int n, k;

void move(int pos){
    if(pos+1 == n) return;
    if(s[pos+1] == 'o') move(pos+1);
    if(s[pos+1] == '.' && s[pos] == 'o'){
        s[pos+1] = 'o';
        s[pos] = '.';
    }
}

void solve(){
    cin >> n >> k;
    cin >> s;

        for(int i = 0; i < k; i++){
            if(i == n) break;
            if(s[i+1] == 'o') move(i);
            if(s[i+1] == '.'){
                s[i] = '.';
                s[i+1] = '#';
            }
        }
        for(char c : s) cout << c;
        cout << "\n";       
}

int main(){
    cin.tie(0) -> sync_with_stdio(0);
    solve();

    return 0;
}