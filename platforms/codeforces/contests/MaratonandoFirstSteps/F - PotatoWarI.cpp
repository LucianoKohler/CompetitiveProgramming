#include<bits/stdc++.h>
using namespace std;
#define ll long long
const ll modulo = 1e9+7;
const int mx = 2e5+5; // This varies!

int main(){
    cin.tie(0) -> sync_with_stdio(0);
    int t, tD, c, cD; cin >> t >> tD >> c >> cD;
    if(t > c){
      cout << "0\n";
      return 0;
    }

    if(tD <= cD){
      cout << "-1\n";
      return 0;
    }

    int sumT = t;
    int sumC = c;
    int days = 0;

    while(sumT <= sumC){
      sumT+=tD;
      sumC+=cD;
      days++;
    }

    cout << days << "\n";
    return 0;
}
