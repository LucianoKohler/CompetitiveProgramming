#include<bits/stdc++.h>
using namespace std;
#define ll long long
const ll modulo = 1e9+7;
const int mx = 501;
ll dists[mx][mx];
ll adj[mx][mx];
int n;

void floydWarshall(){
    for(int i = 1; i <= n; i++){
        for(int j = 1; j <= n; j++){
            if(i == j)              dists[i][j] = 0;
            else if(adj[i][j] != 0) dists[i][j] = adj[i][j];
            else                    dists[i][j] = 1e18;
        }
    }

    for(int k = 1; k <= n; k++){
        for(int i = 1; i <= n; i++){
            for(int j = 1; j <= n; j++){
                dists[i][j] = min(dists[i][j], dists[i][k] + dists[k][j]);
            }
        }
    }

}

int main(){
    cin.tie(0)->sync_with_stdio(0);

    ll m, q; cin >> n >> m >> q;
    for(int i = 0; i < m; i++){
        ll a, b, w; cin >> a >> b >> w;
            if(adj[a][b] == 0){       
                adj[a][b] = w;
                adj[b][a] = w;
            }else if(adj[a][b] > w){ // Overwrite if we have more than 1 roads
                adj[a][b] = w;
                adj[b][a] = w;
            }
        }

    floydWarshall();

    while(q--){
        int a, b; cin >> a >> b;
        if(dists[a][b] == 1e18) cout << "-1\n";
        else                    cout << dists[a][b] << "\n";
    }
    return 0;
}

